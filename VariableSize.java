public class VariableSize {
    public static void main(String[] args)
    {
        System.out.println("byte 변수 범위: " + Byte.MIN_VALUE + " ~ " + Byte.MAX_VALUE);
        System.out.println("short 변수 범위: " + Short.MIN_VALUE + " ~ " + Short.MAX_VALUE);
        System.out.println("int 변수 범위: " + Integer.MIN_VALUE + " ~ " + Integer.MAX_VALUE);
        System.out.println("long 변수 범위: " + Long.MIN_VALUE + " ~ " + Long.MAX_VALUE);
        
        System.out.println("float 변수 범위: " + Float.MIN_VALUE + " ~ " + Float.MAX_VALUE);
        System.out.println("double 변수 범위: " + Double.MIN_VALUE + " ~ " + Double.MAX_VALUE);
    }
}
